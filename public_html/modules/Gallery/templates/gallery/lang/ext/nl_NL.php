<h3>Upload en herschaal foto\'s</h3>
<p>De Gallery module heeft een eigen upload functie. Het maakt gebruik van de Plupload library om meerdere foto\'s tegelijk te kunnen uploaden d.m.v. ctrl/shift-selectie in het dialoogvenster. Om dit te laten werken moet de internetbrowser HTML5 ondersteunen of beschikken over een recente Flash Player plugin.</p>
<p>Om te voorkomen dat gebruikers te grote afbeeldingen uploaden, kunnen maximum afmetingen worden ingesteld in de Opties tab. De grote afbeeldingen worden vóór het uploaden aan de client-zijde herschaald. De verhoudingen van de originele afbeelding blijven daarbij altijd behouden.</p>

<h3>Extra Velden</h3>
<p>Binnen de Gallery module kunnen vele extra velden gedefinieerd worden. Zo\'n extra veld geldt voor een galerie of voor een afbeelding.</p>
<p>Galerie-gerelateerde velden kunnen in het Gallery-sjabloon worden aangeroepen met <code>{$fields.jouw_veld_alias.name}</code> en <code>{$fields.jouw_veld_alias.value}</code>. Het is natuurlijk ook mogelijk om alle velden d.m.v. een foreach loop op <code>{$fields}</code> te tonen.</p>
<p>Afbeelding- en subgalerie-gerelateerde velden kunnen binnen de images foreach loop in het Gallery-sjabloon worden aangeroepen met <code>{$image->fields.jouw_veld_alias.name}</code> en <code>{$image->fields.jouw_veld_alias.value}</code>.</p>


<h3>Wijzig, importeer en exporteer Gallery sjablonen</h3>
<p>Om sjablonen te kunnen bewerken, moet een gebruiker lid zijn van een groep met \'Modify Templates\' rechten.</p>
<p>Gallery sjablonen kunnen afzonderlijk worden gedownload vanaf de <a href="http://dev.cmsmadesimple.org/project/files/726" target="_blank">Gallery module Forge</a>. De importsessie zal het bestaande module sjabloon overschrijven als het aankruisvakje daarvoor aangevinkt is, en het sjabloon dezelfde naam heeft. Merk op dat een update van de Gallery module nooit een verandering aan de module sjablonen zal aanbrengen.</p>
<p>Als u uw eigen sjablonen voor Gallery wilt maken en deze wilt kunnen exporteren/importeren, zorg dan dat benodigde bestanden (uitgezonderd de standaard aanwezige) in een directory worden opgeslagen met dezelfde naam als de naam van het sjabloon, in de directory modules/Gallery/templates/. Deze bestanden worden automatisch toegevoegd aan de xml exportfile. Klik op de \'Over\'-link om het versienummer en de \'over\'-tekst te bewerken.</p>
<p>Sjablonen kunnen worden gemarkeerd als onzichtbaar in het sjabloon keuzeveld, zodat ze niet kunnen worden gekozen door normale admin-gebruikers, maar alleen door gebruikers met \'Modify Templates\' rechten.</p>

<h3>Globale Gallery opties</h3>
<p>Om de Gallery opties in te stellen, moet een gebruiker tot een groep behoren die \'Modify Site Preferences\' rechten heeft.</p>

<h3>Zoekfunctionaliteit</h3>
<p>De Search module zal standaard de links tonen naar subgalerieën die de opgegeven zoekwoorden bevatten in de galerie-velden of de onderliggende afbeeldings-velden.</p>
<p>Om dat te wijzigen kan de optie \'Zoek separate afbeeldingen in plaats van subgalerieën\' aangevinkt worden in het tabblad Opties.</p>
<p>Met wat Smarty logica kunnen zelfs de afbeeldingen in het zoekresultaat worden weergegeven, in plaats van alleen links. Pas hiervoor het \'Resultaat Sjabloon\' aan in de Search module admin.</p>
<p>Bijvoorbeeld wijzig:</p><pre><li>{$entry->title} - <a href="{$entry->url}">{$entry->urltxt}</a> ({$entry->weight}%)</li></pre>
<p>naar:</p><pre><li>{if $entry->title == \'Gallery image\'}{Gallery img=$entry->modulerecord}{else}
